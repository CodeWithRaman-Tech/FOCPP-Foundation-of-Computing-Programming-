'''
9.The integer part of 9.9999 and the rounded value to 2 decimal places.
'''
# Given number
number = 9.9999

# Integer part
integer_part = int(number)

# Rounded value to 2 decimal places
rounded_value = round(number, 2)

# Display the results
print("Integer part:", integer_part)
print("Rounded value (2 decimal places):", rounded_value)
